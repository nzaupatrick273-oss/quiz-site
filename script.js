// KEYBOARD SHORTCUTS
// ============================================
document.addEventListener('keydown', (e) => {
    if (e.key >= '1' && e.key <= '4' && state.gameActive && !state.isPaused) {
        const btns = DOM.answers.querySelectorAll('.answer-btn');
        const index = parseInt(e.key) - 1;
        if (btns[index] && !btns[index].classList.contains('disabled')) {
            selectAnswer(btns[index]);
        }
    }

    if (e.key === ' ' && state.gameActive) {
        e.preventDefault();
        togglePause();
    }

    if (e.key === 'Enter' && !state.gameActive) {
        const startBtn = document.querySelector('.btn-primary');
        if (startBtn && startBtn.textContent.includes('Start')) {
            startSelectedQuiz();
        }
    }
});

